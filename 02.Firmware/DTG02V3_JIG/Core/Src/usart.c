static void delay(uint32_t count)
{
    while (count--);
}
void usart_rs232_send_data(uint8_t *data, uint32_t length)
{
    LL_GPIO_ResetOutputPin(RS485_DIR_GPIO_Port, RS485_DIR_Pin);
    delay(0xFF);
    for (uint32_t i = 0; i < length; i++)
	{
		LL_USART_TransmitData8(USART3, data[i]);
		while (0 == LL_USART_IsActiveFlag_TC(USART3));
	}
    LL_GPIO_SetOutputPin(RS485_DIR_GPIO_Port, RS485_DIR_Pin);
}

